#Upper Case
a = "Hello, World!"
print(a.upper())
#Lower Case
a = "Hello, World!"
print(a.lower())
#Remove Whitespace
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"

#Split 
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']
#Replace
a = "Hello, World!"
print(a.replace("H", "J"))