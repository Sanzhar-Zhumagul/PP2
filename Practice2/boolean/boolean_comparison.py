# Операторы сравнения используются для сравнения значений
# Результат любого сравнения — True или False

a = 15
b = 10

print(a > b)    # больше
print(a < b)    # меньше
print(a == b)   # равно
print(a != b)   # не равно
print(a >= b)   # больше или равно
print(a <= b)   # меньше или равно

# Сравнивать можно не только числа
print("apple" == "apple")
print("apple" != "banana")