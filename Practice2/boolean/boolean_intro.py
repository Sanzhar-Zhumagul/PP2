# Boolean (bool) — это логический тип данных
# Он может иметь только два значения: True или False

is_student = True
is_employed = False

print(is_student)
print(is_employed)

# Boolean часто используется в условиях
print(type(is_student))
print(type(is_employed))

# Boolean может быть результатом выражения
result = 5 > 3
print(result)